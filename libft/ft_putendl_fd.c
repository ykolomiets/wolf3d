#include "libft.h"
#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	if (!s)
		return ;
	if (fd < 1)
		return ;
	write(fd, s, ft_strlen(s));
	write(fd, "\n", 1);
}
